    <script>
        document.addEventListener("DOMContentLoaded", function () {
    const slides = document.querySelectorAll(".slide");
    let currentIndex = 0;
    const totalSlides = slides.length;
    const slideInterval = 5000; // Time before next slide

    function showSlide(index) {
        slides.forEach((slide, i) => {
            slide.style.opacity = i === index ? "1" : "0";
            slide.style.transform = i === index ? "translateX(0)" : "translateX(100%)";
        });

        const currentText = slides[index].querySelector(".slide_text");
        currentText.style.opacity = "0";
        currentText.style.transform = "translateY(50px)";

        setTimeout(() => {
            currentText.style.opacity = "1";
            currentText.style.transform = "translateY(0)";
        }, 1000); // Text appears after background
    }

    function nextSlide() {
        currentIndex = (currentIndex + 1) % totalSlides;
        showSlide(currentIndex);
    }

    setInterval(nextSlide, slideInterval);
    showSlide(currentIndex);
});

